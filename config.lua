-- config.lua
-- Configuration settings for the editor

local config = {}

-- Font size used in the editor
config.fontSize = 14

-- Width of the background for line numbers
config.lineNumberBackgroundWidth = 40

-- Blink speed of the cursor in seconds (e.g., 0.5 seconds)
config.blink_speed = 0.5

-- Splitter position ratio: proportion of the screen width allocated to the file tree
-- Values between 0 and 1, where 0.3 means 30% of the screen width is used for the file tree
config.splitterPosition = 0.3

return config
